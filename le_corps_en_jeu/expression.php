<p>Le destin du corps est de percevoir, de se mouvoir, de go&ucirc;ter et d'exprimer la vie qui le traverse. Le corps se meut de l'int&eacute;rieur vers l'ext&eacute;rieur pour communiquer et de l'ext&eacute;rieur vers l'int&eacute;rieur pour conna&icirc;tre.<br />
&nbsp;</p>
